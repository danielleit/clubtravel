package models;

public class Region {
    private int idRegion;
    private String name;
    private String uf;
    
    public int getIdRegion() {
        return idRegion;
    }
    public void setIdRegion(int idRegion) {
        this.idRegion = idRegion;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public String getUf() {
        return uf;
    }
    public void setUf(String uf) {
        this.uf = uf;
    }
    
}
